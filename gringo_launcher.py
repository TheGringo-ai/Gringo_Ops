if tool == "Chat":
    st.subheader("ğŸ’¬ Chat Assistant")
    model = st.selectbox("Choose LLM", ["OpenAI (GPT-4)", "Gemini"])
    user_input = st.text_area("Enter your message:")
    if st.button("Send"):
        if model.startswith("OpenAI"):
            import openai
            client = openai.OpenAI(api_key=os.getenv("OPENAI_API_KEY"))
            response = client.chat.completions.create(
                model=conf["openai"]["model"],
                messages=[
                    {"role": "system", "content": "You are a helpful assistant."},
                    {"role": "user", "content": user_input}
                ]
            )
            st.markdown(f"**GPT-4 Response:**\n\n{response.choices[0].message.content}")
        else:
            st.write("ğŸ§  Sending to Gemini...")
            gemini_query.query_model(prompt=user_input)

